    //Recipients
    $mail->setFrom('info@cyberajans.com', 'Mailer');
    $mail->addAddress('info@cyberajans.com', 'Joe User');     //Add a recipient
    //$mail->addAddress('ellen@example.com');               //Name is optional
   // $mail->addReplyTo('info@example.com', 'Information');
